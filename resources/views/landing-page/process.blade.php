    function switchWorkflowStep(index, targetTab = currentTab) {
        const isUmkm = targetTab === 'umkm';
        const prefix = isUmkm ? 'workflow' : 'workflow-customer';

        const indicator = document.getElementById(`${prefix}-indicator`);
        if(indicator) indicator.style.top = (index * 25) + "%";

        for (let i = 0; i < 4; i++) {
            const img = document.getElementById(`${prefix}-img-${i}`);
            if(!img) continue;
            
            if (i === index) {
                img.style.opacity = '1';
                img.style.transform = 'scale(1)';
                img.style.zIndex = '10';
            } else {
                img.style.opacity = '0';
                img.style.transform = 'scale(1.05)';
                img.style.zIndex = '0';
            }
        }

        const container = document.getElementById(`content-${targetTab}`);
        if(!container) return;
        
        const steps = container.querySelectorAll('.workflow-step-btn');
        steps.forEach((step, i) => {
            const textBlock = step.querySelector('.workflow-text');
            const iconOutline = step.querySelector('.icon-outline');
            const iconSolid = step.querySelector('.icon-solid');
            
            if (i === index) {
                textBlock.classList.remove('opacity-60');
                textBlock.classList.add('opacity-100');
                iconOutline.classList.replace('opacity-100', 'opacity-0');
                iconSolid.classList.replace('opacity-0', 'opacity-100');
            } else {
                textBlock.classList.add('opacity-60');
                textBlock.classList.remove('opacity-100');
                iconOutline.classList.replace('opacity-0', 'opacity-100');
                iconSolid.classList.replace('opacity-100', 'opacity-0');
            }
        });
    }
    
